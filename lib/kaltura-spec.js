function resolveRef(base, schema) {
  var ref = schema.$ref;
  if (!ref) return schema;
  ref = ref.substring('#/definitions/'.length);
  return base.definitions[ref];
}

function fixMarkdown(str) {
  if (!str) return;
  var ret = str.replace(/\n\s*\t\s*/g, '\n\n').trim();
  return ret || undefined;
}
